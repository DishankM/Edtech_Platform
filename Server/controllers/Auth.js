exports.sendOTP = async (req, res) => {
  try {
    // Fetch email from request body
    const { email } = req.body;

    // Check if user already exists
    const checkUserPresent = await User.findOne({ email });

    if (checkUserPresent) {
      return res.status(400).json({
        success: false,
        message: "User already registered...",
      });
    }

    // Generate OTP
    let otp = otpGenerator.generate(6, {
      upperCaseAlphabets: false,
      lowerCaseAlphabets: false,
      specialChars: false,
    });

    // Ensure the OTP is unique
    let result = await OTP.findOne({ otp });

    while (result) {
      otp = otpGenerator.generate(6, {
        upperCaseAlphabets: false,
        lowerCaseAlphabets: false,
        specialChars: false,
      });
      result = await OTP.findOne({ otp });
    }

    // Create the OTP entry in the database
    const otpPayload = { email, otp };
    const otpBody = await OTP.create(otpPayload);
    console.log(otpBody);

    // Send OTP email
    const emailContent = otpTemplate(otp); // Use the otpTemplate function to generate the HTML email
    const mailOptions = await mailSender(
      email,  // Recipient's email
      `OTP for Course Registration `,
      emailContent,  // The HTML content generated by the otpTemplate function
    );

    // Send the email using the mailSender utility
    // await mailSender(mailOptions);

    // Return response successfully
    res.status(200).json({
      success: true,
      message: 'OTP Sent Successfully',
      otp, // You might not want to send the OTP in the response body, for security reasons
    });

  } catch (error) {
    console.log(error);
    return res.json({
      success: false,
      message: error.message,
    });
  }
};


